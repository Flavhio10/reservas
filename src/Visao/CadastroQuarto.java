    private void btnGravarQuartoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarQuartoActionPerformed
        if (this.txtNumeroQuarto.getText().equals("") || this.txtDescricaoQuarto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preenchimento de campos obrigatório!");
        } else {
            int numero = Integer.parseInt(this.txtNumeroQuarto.getText());
            String descricao = this.txtDescricaoQuarto.getText();
            new ControleQuartos().cadastrarQuartos(new Quartos(numero, descricao));
            this.txtNumeroQuarto.setText("");
            this.txtDescricaoQuarto.setText("");
            lerTabela();
        }
    }//GEN-LAST:event_btnGravarQuartoActionPerformed


