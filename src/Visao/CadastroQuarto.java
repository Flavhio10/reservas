    public void lerTabela() {

        List<Quartos> listQuartos = new ArrayList();
        listQuartos.addAll(new QuartoDAO().listarTodos());

        DefaultTableModel modelo = (DefaultTableModel) this.tabelaQuartos.getModel();
        this.tabelaQuartos.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        for (Quartos q : listQuartos) {
            modelo.addRow(new Object[]{q.getId(), q.getNumero(), q.isTipo()});
        }
    }

