    private void btnGravarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarClienteActionPerformed
        if (this.txtNomeCliente.getText().equals("") || this.txtCpf.getText().equals("")
                || this.txtEmail.getText().equals("") || this.txtCel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preenchimento de campos obrigatório!");
        } else {
            try {
                String nome = this.txtNomeCliente.getText();
                long cpf = Long.parseLong(this.txtCpf.getText());
                String email = this.txtEmail.getText();
                String cel = this.txtCel.getText();

                new ControleCliente().cadastraClientes(new Clientes(nome, cpf, email, cel));
                
                
                this.txtNomeCliente.setText("");
                this.txtCpf.setText("");
                this.txtEmail.setText("");
                this.txtCel.setText("");
                lerTabela();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getStackTrace());
            }
        }
    }//GEN-LAST:event_btnGravarClienteActionPerformed


