    public void lerTabela() {

        List<Clientes> listClientes = new ArrayList();
        listClientes.addAll(new ClienteDAO().listarTodos());

        DefaultTableModel modelo = (DefaultTableModel) this.jTable2.getModel();
        this.jTable2.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        for (Clientes c : listClientes) {
            modelo.addRow(new Object[]{c.getId(), c.getNome(), c.getCpf(), c.getEmail(), c.getTelefone()});
        }
    }

